<template>
  <div class="flex flex-col px-[23px] py-[10px] h-full">
    <PageHeader :title="service.title" class="mb-[60px]"/>
    <ServiceGrid :services="service.children" :circle="false" :is-link="false"/>
  </div>
</template>

<script setup lang="ts">

import PageHeader from "~/components/header/PageHeader.vue";
import {useCustomFetch} from "../../composables/useCustomFetch";

definePageMeta({
  layout: 'default'
})

const route = useRoute()
const service = ref<IService>()
const id = route.params.id
const getService = async () => {
  const res = await useCustomFetch(`/services/${id}`,{
    method: "GET"
  })
  if (res.data.value) {
    service.value = res.data.value?.data as IService
  }
}

await getService()
</script>

